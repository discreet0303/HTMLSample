int to_int(uint8_t hex_1, uint8_t hex_2){
  return hex_1 * 16 + hex_2;
}

int to_hex(uint8_t num, int place){
  return (place == 1)? num / 16: num % 16;
}

uint8_t generate_Rcon(exp){
  uint8_t root = 0x01;
  for (int i = 1;i < exp;i ++){
    root *= 2;
  }
  return root;
}

